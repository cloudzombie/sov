//! A real CPU proof-of-work miner for SOV — and the honest source of the
//! dashboard's mining data.
//!
//! Nakamoto consensus: **producing a block IS the mining.** When you run this,
//! your machine grinds the block header's double-SHA-256 against the chain's
//! live difficulty until the seal meets the target; the block's coinbase then
//! mints the scheduled reward (under the 21M cap) to your miner account. Your
//! real mining session is written to `dashboard/chain-status.js`
//! (`window.SOV_CHAIN`). It only ever reports this one miner — the one you are
//! actually running. Nothing is simulated: no miner exists in that file unless
//! you mined it here.
//!
//! Run: `cargo run --release -p sov-node --bin sov-miner [blocks]`
//! (release matters — mining is real work).

use std::path::Path;
use std::time::{Instant, SystemTime, UNIX_EPOCH};

use sov_chain::{Blockchain, GenesisAccount, GenesisConfig};
use sov_crypto::Keypair;
use sov_mining::MiningPolicy;
use sov_node::Node;
use sov_pow::Target;
use sov_primitives::{AccountId, Balance, Hash};

/// Your miner account — the one this process actually mines with.
const SELF_MINER: &str = "creator.sov";

fn id(s: &str) -> AccountId {
    AccountId::new(s).expect("valid account id")
}

fn now_ms() -> u64 {
    SystemTime::now()
        .duration_since(UNIX_EPOCH)
        .unwrap()
        .as_millis() as u64
}

fn short(h: &Hash) -> String {
    let hex = h.to_hex();
    format!("0x{}…{}", &hex[..8], &hex[hex.len() - 6..])
}

/// Write the real mining session to the dashboard's data file. Reflects only the
/// miners that have actually mined on this chain (here, just yours).
fn write_snapshot(node: &Node, path: &Path) {
    let chain = node.chain();
    let miners: Vec<String> = chain
        .miner_registry()
        .iter()
        .map(|m| {
            let balance = chain.ledger().account(&m.account).balance;
            format!(
                "{{\"account\":\"{}\",\"firstSeenHeight\":{},\"firstSeenUnixMs\":{},\"blocksMined\":{},\"lastSeenHeight\":{},\"balance\":\"{}\",\"isSelf\":{}}}",
                m.account,
                m.first_seen_height,
                m.first_seen_timestamp_ms,
                m.blocks_mined,
                m.last_seen_height,
                balance,
                m.account.as_str() == SELF_MINER
            )
        })
        .collect();
    let js = format!(
        "// AUTO-GENERATED by `sov-miner` from your live mining session. Real data; do not edit.\nwindow.SOV_CHAIN = {{\"chainId\":\"{}\",\"generatedAtUnixMs\":{},\"height\":{},\"totalSupply\":\"{}\",\"sha256dDifficulty\":\"{}\",\"selfMiner\":\"{}\",\"miners\":[{}]}};\n",
        chain.chain_id(),
        now_ms(),
        chain.height(),
        chain.ledger().total_supply().unwrap(),
        chain.sha256d_difficulty().0,
        SELF_MINER,
        miners.join(",")
    );
    if let Err(e) = std::fs::write(path, js) {
        eprintln!("warning: could not write dashboard snapshot: {e}");
    }
}

fn main() {
    let blocks: u64 = std::env::args()
        .nth(1)
        .and_then(|a| a.parse().ok())
        .unwrap_or(10);

    let validator = Keypair::from_seed([1; 32]);
    let miner_key = Keypair::from_seed([2; 32]);
    let miner = id(SELF_MINER);

    // The REAL mainnet emission (50-SOV subsidy halving every 210,000 blocks,
    // budget = the full 21M cap, NO pre-mine) with only the demo knobs changed:
    // a laptop-friendly difficulty and 1s cadence, fees off. The seal is forced
    // to SHA-256d so this single-process demo mines instantly (mainnet uses
    // RandomX, which is memory-hard by design).
    let mining = MiningPolicy {
        pow_algo: sov_pow::PowAlgo::Sha256d,
        sha256d_target: Target::from_leading_zero_bits(22),
        target_block_ms: 1_000,
        gas_price: Balance::ZERO,
        deshield_window_blocks: 0,
        deshield_limit_grains: 0,
        ..MiningPolicy::mainnet_like()
    };
    let config = GenesisConfig {
        chain_id: "sov-devnet".into(),
        timestamp_ms: now_ms(),
        accounts: vec![
            GenesisAccount {
                account: id("val01.node.sov"),
                key: validator.public_key(),
                balance: Balance::ZERO,
            },
            // The miner identity: a keyed, zero-balance account the coinbase
            // pays. (Coinbase recipients must hold a key to spend.)
            GenesisAccount {
                account: miner.clone(),
                key: miner_key.public_key(),
                balance: Balance::ZERO,
            },
        ],
        mining,
        vesting: vec![],
    };

    let mut node = Node::new(Blockchain::new(&config).expect("genesis builds"), 1024, 256);
    // Every block this node mines credits its coinbase to YOUR miner account.
    node.set_coinbase(miner.clone());

    let snapshot_path =
        Path::new(env!("CARGO_MANIFEST_DIR")).join("../../../dashboard/chain-status.js");

    println!("=== SOV real-compute miner (Nakamoto) ===");
    println!("mining as     {miner} (SHA-256d header grind) · cap 21,000,000 XUS");
    println!("dashboard     dashboard/chain-status.js (reload the page to watch)\n");
    write_snapshot(&node, &snapshot_path);

    for _ in 0..blocks {
        let difficulty = node.chain().sha256d_difficulty();

        // ---- REAL WORK: produce() grinds the header's double-SHA-256 until
        // the seal meets the live target; the committed nonce counts the
        // attempts this candidate took. ----
        let start = Instant::now();
        let produced = node.produce(now_ms()).expect("block mined");
        let elapsed = start.elapsed();
        let hashes = produced.block.header.nonce + 1;
        let hps = (hashes as f64 / elapsed.as_secs_f64().max(1e-9)) as u64;

        write_snapshot(&node, &snapshot_path);

        println!(
            "block #{:<2} {}",
            produced.block.header.height.get(),
            short(&produced.block.hash())
        );
        println!("  difficulty   {}", difficulty.0);
        println!("  work         {hashes} hashes in {elapsed:?}  (~{hps} H/s)");
        println!(
            "  coinbase     {} -> {}",
            node.chain().mint_reward(),
            produced.block.header.proposer
        );
        println!(
            "  balance      {}",
            node.chain().ledger().account(&miner).balance
        );
        println!(
            "  confirmations {} (final at {})\n",
            node.chain()
                .confirmations(&produced.block.hash())
                .unwrap_or(0),
            sov_chain::FINALITY_DEPTH
        );
    }

    println!("done — {blocks} blocks mined as {miner}; dashboard snapshot updated.");
}
